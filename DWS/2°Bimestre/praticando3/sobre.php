<p>
   Ben 10: Força Alienígena é a continuação do desenho animado Ben 10. Criado por Dwayne McDuffie e Glen Murakami no estúdio "Man of Action" (um grupo composto por Duncan Rouleau, Joe Casey, Joe Kelly, Dan Riba E Steven T. Seagle), e produzida pelo Cartoon Network Studios. A série acontece cinco anos após a série original.

Força Alienígena estreou no Cartoon Network em 18 de abril de 2008 nos Estados Unidos, e no Brasil chegou em junho e começou a ser exibida no SBT em 12 de junho de 2010, embora no SBT tenha tido muitas reexibições. A série foi originalmente produzida sob o título de trabalho Ben 10: Hero Generation, em uma tradução livre, Ben 10: Geração de Heróis.
